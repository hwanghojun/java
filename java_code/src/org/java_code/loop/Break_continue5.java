package org.java_code.loop;

public class Break_continue5 {

	public static void main(String[] args) {
		for(int i=0; i<5;++i) {
			for(int j=0; j<5; ++j) {
				System.out.print(i+1+" ");
			}
			System.out.println();
		}
		
		for(int i=0; i<5;++i) {
			for(int j=0; j<5; ++j) {
				System.out.print(j+1+" ");
			}
			System.out.println();
		}

	}

}
